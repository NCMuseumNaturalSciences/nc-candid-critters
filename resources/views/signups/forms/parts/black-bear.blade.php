<h3>Black Bear Precautions</h3>
<fieldset>
    <p>Please be aware that there is a high density of black bears in eastern and western North Carolina. Black bears can cause damage to your camera trap. To try to prevent bear damage, you can either:</p>
    <ol>
        <li>Make sure you do not get any food scent on your camera (e.g., do not eat before setting your camera)</li>
        <li>Purchase a bear box to secure your camera*</li>
    </ol>
    <p><em>*This is not necessary, and we will not be putting boxes on all of our cameras located in bear-populated areas, but know that it is an option. Bear boxes can be purchased through Amazon or similar retailers.</em></p>

</fieldset>